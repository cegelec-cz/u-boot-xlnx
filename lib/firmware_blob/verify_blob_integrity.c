static inline int is_aligned(const void *pointer, uint32_t alignment)
{
  return (uintptr_t) pointer % alignment == 0;
}

static inline uint32_t get_blob_data_len(const struct blob_header *header)
{
  return sizeof(*header) + header->package_identifier_len +
         header->bitstream_len + header->elf_len;
}

validate_blob_err_t
verify_blob_integrity(const void *blob, uint32_t blob_len,
                      struct blob_header *parsed_blob_header_out,
                      int *has_valid_signature)
{
  *has_valid_signature = 0;

  if (!is_aligned(blob, BLOB_ALIGNMENT))
    return VALIDATE_BLOB_ERR_INTEGRITY_UNALIGNED_BLOB;

  if (blob_len < sizeof(*parsed_blob_header_out))
    return VALIDATE_BLOB_ERR_INTEGRITY_INVALID_BLOB_LEN;

  memcpy(parsed_blob_header_out, blob, sizeof(*parsed_blob_header_out));

  if (parsed_blob_header_out->magic != BLOB_MAGIC)
    return VALIDATE_BLOB_ERR_INTEGRITY_INVALID_MAGIC;

  if (parsed_blob_header_out->blob_size != blob_len)
    return VALIDATE_BLOB_ERR_INTEGRITY_INVALID_BLOB_LEN;

  uint32_t blob_data_len = get_blob_data_len(parsed_blob_header_out);

  // Blob contains data after footer, attempt to parse and verify signature
  if (blob_len > blob_data_len + sizeof(struct blob_footer) +
                     sizeof(struct vb21_signature)) {
    uint8_t sig_buffer[BLOB_SIG_BUFFER_LEN];
    struct vb21_signature *sig_header = (struct vb21_signature *) sig_buffer;

    // Copy signature to stack buffer to guarantee signature alignment
    memcpy(sig_header, blob + blob_data_len + sizeof(struct blob_footer),
           sizeof(*sig_header));

    // Signature headers data size sanity check
    uint32_t sig_len = blob_len - blob_data_len - sizeof(struct blob_footer);
    if (sig_header->c.total_size != sig_len ||
        sig_header->c.fixed_size != sizeof(*sig_header) ||
        sig_header->sig_offset >= sig_len || // Prevention of uint32_t overflow
        sig_header->sig_size >
            sig_len - sizeof(*sig_header) || // Prevention of uint32_t overflow
        sig_header->sig_offset + sig_header->sig_size != sig_len ||
        sig_header->data_size != blob_data_len + sizeof(struct blob_footer)) {
      return VALIDATE_BLOB_ERR_INTEGRITY_MALFORMED_SIG;
    }

    if (sig_len > sizeof(sig_buffer))
      return VALIDATE_BLOB_ERR_INTEGRITY_TOO_MANY_SIG_BYTES;

    memcpy(sig_buffer + sizeof(*sig_header),
           blob + blob_data_len + sizeof(struct blob_footer) +
               sizeof(*sig_header),
           sig_len - sizeof(*sig_header));

    if (vblock_check_signature(blob,
                               (const struct vb21_packed_key *) vb21_public_key,
                               (const struct vb21_signature *) sig_buffer)) {
      *has_valid_signature = 1;
      return VALIDATE_BLOB_ERR_NONE;
    } else
      return VALIDATE_BLOB_ERR_INTEGRITY_VERIFICATION_FAILED;
  }
  // Blob contains only SHA-256 hash
  else if (blob_len == blob_data_len + sizeof(struct blob_footer)) {
    struct sha256_ctx ctx;
    SHA256_init(&ctx);
    SHA256_update(&ctx, blob, blob_data_len);
    uint8_t *hash = SHA256_final(&ctx);

    struct blob_footer blob_footer;
    memcpy(&blob_footer, blob + blob_data_len, sizeof(blob_footer));

    if (memcmp(hash, blob_footer.hash, SHA256_DIGEST_SIZE) == 0)
      return VALIDATE_BLOB_ERR_NONE;
    else
      return VALIDATE_BLOB_ERR_INTEGRITY_VERIFICATION_FAILED;
  } else
    return VALIDATE_BLOB_ERR_INTEGRITY_INVALID_BLOB_LEN;
}